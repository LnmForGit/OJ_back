//加载结果列表
function loadResultList(t) {
    var dataTable = $('#resultTable');
    if ($.fn.dataTable.isDataTable(dataTable)) {
        dataTable.DataTable().destroy();
    }
    dataTable.DataTable({
        // "searching":false,
        //"paging": false,
        "serverSide": false,
        "autoWidth": false,
        "bSort": false,
        //"scrollY": "150px",
        //"scrollCollapse": "true",
        "data": t,
        "columns": [{
            "data": "result"
        }, {
            "data": "Saccount"
        }, {
            "data": "Sname"
        }, {
            "data": "SclassName"
        }, {
            "data": "StestScore"
        }, {
            "data": "Taccount"
        }, {
            "data": "Tname"
        }, {
            "data": "TclassName"
        }, {
            "data": "TtestScore"
        }]
    });
}
